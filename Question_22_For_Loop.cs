/*
 22. Write a program to display the first 10 natural numbers using for loops.
 
 */

using System;


namespace Question_22_While_Loop
{
    internal class Program
    {
        static void Main(string[] args)
        {
            
            for (int x = 1; x <= 10; x++)

            {
                                     
                Console.WriteLine(x);
               
            }
            Console.ReadLine();

        }
    }
}
